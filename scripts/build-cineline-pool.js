#!/usr/bin/env node
/**
 * Build a big, YEAR-BALANCED pool of well-known films for CineLine.
 * For each decade it asks TMDB Discover for the most-voted (recognisable) films
 * of that decade, so the timeline deck spans the whole history of cinema instead
 * of skewing modern. Writes cineline-pool.js.
 *
 * Usage:
 *   npm run cineline-pool                      # ~22 films per decade, 1930s–2020s
 *   node scripts/build-cineline-pool.js --per 30 --min-votes 600
 *
 * Needs TMDB_API_KEY (run `npm run env:pull` first, or pass it inline).
 * Re-run whenever you want a fresher / bigger pool — the file is overwritten.
 */
'use strict';
const fs = require('fs');
const path = require('path');
require('./load-env').loadEnv();

const API_KEY = process.env.TMDB_API_KEY;
const ROOT = path.join(__dirname, '..');
const OUT = path.join(ROOT, 'cineline-pool.js');

function arg(flag, def) {
  const i = process.argv.indexOf(flag);
  return i >= 0 && process.argv[i + 1] ? process.argv[i + 1] : def;
}
const PER_DECADE = parseInt(arg('--per', '22'), 10);
const MIN_VOTES = parseInt(arg('--min-votes', '700'), 10);
const DECADES = [1930, 1940, 1950, 1960, 1970, 1980, 1990, 2000, 2010, 2020];

if (!API_KEY || API_KEY === 'tu_key' || API_KEY === 'xxxxx') {
  console.error('Set a real TMDB_API_KEY (run `npm run env:pull` first).');
  process.exit(1);
}

async function tmdb(p) {
  const url = 'https://api.themoviedb.org/3/' + p + '&api_key=' + API_KEY;
  const res = await fetch(url);
  if (!res.ok) throw new Error('TMDB ' + res.status + ' on ' + p);
  return res.json();
}

async function decade(start) {
  const end = start + 9;
  const out = [];
  for (let page = 1; page <= 3 && out.length < PER_DECADE; page++) {
    const q = 'discover/movie?language=en-US&include_adult=false&sort_by=vote_count.desc' +
      '&vote_count.gte=' + MIN_VOTES +
      '&primary_release_date.gte=' + start + '-01-01' +
      '&primary_release_date.lte=' + end + '-12-31' +
      '&page=' + page;
    const d = await tmdb(q);
    for (const m of d.results || []) {
      if (out.length >= PER_DECADE) break;
      if (m.id && m.poster_path) out.push(m.id);
    }
    if (!d.results || !d.results.length) break;
  }
  console.log('  ' + start + 's -> ' + out.length + ' films');
  return out;
}

async function main() {
  console.log('Building CineLine pool (' + PER_DECADE + '/decade, min ' + MIN_VOTES + ' votes)...');
  const ids = [];
  for (const d of DECADES) {
    try { ids.push(...await decade(d)); } catch (e) { console.error('  ' + d + 's failed: ' + e.message); }
  }
  const uniq = [...new Set(ids)];
  const rows = [];
  for (let i = 0; i < uniq.length; i += 10) rows.push('  ' + uniq.slice(i, i + 10).join(', '));
  const body =
    '// CineLine pool — generated by scripts/build-cineline-pool.js (' + uniq.length + ' films,\n' +
    '// year-balanced across decades). Re-run `npm run cineline-pool` to refresh.\n' +
    'window.CINELINE_POOL = [\n' + rows.join(',\n') + '\n];\n';
  fs.writeFileSync(OUT, body);
  console.log('Wrote ' + uniq.length + ' films to ' + path.basename(OUT));
}

main().catch(e => { console.error(e); process.exit(1); });
