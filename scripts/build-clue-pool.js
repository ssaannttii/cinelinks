#!/usr/bin/env node
/**
 * Build a large, stable pool of well-known movies for CineClue.
 * Pulls several pages of TMDB's most popular + top-rated films, keeps the ones
 * with enough votes (recognisable), and writes cineclue-pool.js.
 *
 * Usage:
 *   npm run clue-pool                 # ~200 films
 *   node scripts/build-clue-pool.js --pages 15 --min-votes 1500
 *
 * Needs TMDB_API_KEY (run `npm run env:pull` first, or pass it inline).
 * Re-run whenever you want fresh/more titles — the file is overwritten.
 */
'use strict';
const fs = require('fs');
const path = require('path');
require('./load-env').loadEnv();

const API_KEY = process.env.TMDB_API_KEY;
const ROOT = path.join(__dirname, '..');
const OUT = path.join(ROOT, 'cineclue-pool.js');

function arg(flag, def) {
  const i = process.argv.indexOf(flag);
  return i >= 0 && process.argv[i + 1] ? process.argv[i + 1] : def;
}
const PAGES = parseInt(arg('--pages', '10'), 10);
const MIN_VOTES = parseInt(arg('--min-votes', '1200'), 10);

if (!API_KEY || API_KEY === 'tu_key' || API_KEY === 'xxxxx') {
  console.error('Set a real TMDB_API_KEY (run `npm run env:pull` first).');
  process.exit(1);
}

async function tmdb(p) {
  const url = 'https://api.themoviedb.org/3/' + p + (p.includes('?') ? '&' : '?') + 'api_key=' + API_KEY;
  const res = await fetch(url);
  if (!res.ok) throw new Error('TMDB ' + res.status + ' on ' + p);
  return res.json();
}

async function collect(listPath) {
  const seen = new Map();
  for (let page = 1; page <= PAGES; page++) {
    const d = await tmdb(listPath + '?language=en-US&page=' + page);
    for (const m of d.results || []) {
      if ((m.vote_count || 0) < MIN_VOTES) continue;
      if (m.adult) continue;
      seen.set(m.id, m.popularity || 0);
    }
  }
  return seen;
}

async function main() {
  console.log(`Building CineClue pool — ${PAGES} pages, min ${MIN_VOTES} votes...`);
  const merged = new Map();
  for (const list of ['movie/top_rated', 'movie/popular']) {
    const m = await collect(list);
    for (const [id, pop] of m) merged.set(id, Math.max(merged.get(id) || 0, pop));
    console.log('  ' + list + ' -> running total ' + merged.size);
  }
  // Sort by popularity desc for a stable, sensible order.
  const ids = [...merged.entries()].sort((a, b) => b[1] - a[1]).map(([id]) => id);

  const rows = [];
  for (let i = 0; i < ids.length; i += 10) rows.push('  ' + ids.slice(i, i + 10).join(', '));
  const body =
    '// CineClue daily pool — generated by scripts/build-clue-pool.js (' + ids.length + ' films).\n' +
    '// Picked deterministically by date. Re-run `npm run clue-pool` to refresh.\n' +
    'window.CINECLUE_POOL = [\n' + rows.join(',\n') + '\n];\n';
  fs.writeFileSync(OUT, body);
  console.log('Wrote ' + ids.length + ' films to ' + path.basename(OUT));
}

main().catch((e) => { console.error(e); process.exit(1); });
